    private void bSEEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSEEActionPerformed
        boolean aux = true;
        switch (bSEE.getText()) {
            case "Incluir":
                try {
                    ag.Incluir(new Contatos("",
                            textNome.getText(),
                            textEnd.getText(),
                            ag.ValidaEmail(textEmail.getText()),
                            ag.ValidaTelefone(textDddRes.getText(), textTelRes.getText(), 0),
                            ag.ValidaTelefone(textDddCom.getText(), textTelCom.getText(), 1)));
                } catch (NumberFormatException ex) {
                    Logger.getLogger(Contato.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(this, "DDD ou Telefone invalido para residencial e/ou comercial!");
                    aux = false;
                } catch (Exception ex) {
                    Logger.getLogger(Contato.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(this, "E-mail invalido!");
                    aux = false;
                }
                break;
            case "Editar": {
                try {
                    ag.Editar(new Contatos(contato.getIndice(),
                            textNome.getText(),
                            textEnd.getText(),
                            ag.ValidaEmail(textEmail.getText()),
                            ag.ValidaTelefone(textDddRes.getText(), textTelRes.getText(), 0),
                            ag.ValidaTelefone(textDddCom.getText(), textTelCom.getText(), 1)));
                } catch (NumberFormatException ex) {
                    Logger.getLogger(Contato.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(this, "DDD ou Telefone invalido para residencial e/ou comercial!");
                    aux = false;
                } catch (Exception ex) {
                    Logger.getLogger(Contato.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(this, "E-mail invalido!");
                    aux = false;
                }
            }
            break;
            case "Excluir":
                ag.Excluir(contato);
                break;
        }
        if (aux) {
            this.setVisible(false);
            m.setVisible(true);
        }
    }//GEN-LAST:event_bSEEActionPerformed

