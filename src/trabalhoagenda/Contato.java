    public void doScreen(int i) {
        aux = i;
        contato = contatos.get(i);
        this.textNome.setText(contato.getNome());
        this.textEnd.setText(contato.getEndereco());
        this.textEmail.setText(contato.getEmail());
        this.textDddRes.setText(contato.getTelres().split("#")[0]);
        this.textTelRes.setText(contato.getTelres().split("#")[1]);
        this.textDddCom.setText(contato.getTelcom().split("#")[0]);
        this.textTelCom.setText(contato.getTelcom().split("#")[1]);
    }

